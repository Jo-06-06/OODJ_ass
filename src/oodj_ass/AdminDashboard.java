    private String generateNextUserId(String role) {
        String prefix;
        if (role.equalsIgnoreCase("Admin")) {
            prefix = "ADM";
        } else {
            prefix = "AO";
        }
        
        int maxId = 0;
        
        for (User u : userManager.getAllUsers()) {
            if (u.getUserId().startsWith(prefix)) {
                try {
                    int numericPart = Integer.parseInt(u.getUserId().substring(prefix.length()));
                    if (numericPart > maxId) maxId = numericPart;
                } catch (NumberFormatException e) {
                }
            }
        }

        return String.format("%s%03d", prefix, maxId + 1);
    }

    private boolean isValidEmail(String email) {
        return email.matches("^[\\w-\\.]+@([\\w-]+\\.)+[\\w-]{2,4}$");
    }
